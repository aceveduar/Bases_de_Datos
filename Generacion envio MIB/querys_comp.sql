SELECT CONEDO,COUNT(DISTINCT CONCALT)
FROM  MP_PRMALTASM M,MP_PRMALTASH
WHERE CONCALT=HIJCALT
and   CONMAR=HIJMAR
AND   CONPRM=HIJPRM
AND  CONCALT IN (SELECT CON_ID FROM ALTAS WHERE CONMUESTRA='GP03')
AND (CONEDAD1 IS NOT NULL OR CONEDAD2 IS NOT NULL OR CONEDAD3 IS NOT NULL OR CONEDAD4 IS NOT NULL OR MONTHS_BETWEEN(SYSDATE,HIJFNA) BETWEEN 12 AND 36)
GROUP BY CONEDO