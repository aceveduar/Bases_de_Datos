SELECT NVL(NUM_COD,0) +1, COUNT(*) FROM ALTAS
WHERE   CONSUS IS NULL
GROUP BY NVL(NUM_COD,0)+1

SELECT NVL(NUM_COD,0) +1, COUNT(*) FROM ALTAS
WHERE   CONMUESTRA IS NULL
GROUP BY NVL(NUM_COD,0)+1

